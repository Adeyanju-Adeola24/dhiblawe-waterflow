async function getTableData(table, includeRowIdx = false) {
  if (!cache[table]) cache[table] = await getAllRows(table);
  const rows = cache[table];
  if (includeRowIdx) return rows;
  return rows.map(({ __rowIndex, ...rest }) => rest);
}
